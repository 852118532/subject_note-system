package com.zr.model;

public class SubLabel {
	private int SubLabelId;
	private String SubLabelName;
	private int SubCount;
	public int getSubLabelId() {
		return SubLabelId;
	}
	public void setSubLabelId(int subLabelId) {
		SubLabelId = subLabelId;
	}
	public String getSubLabelName() {
		return SubLabelName;
	}
	public void setSubLabelName(String subLabelName) {
		SubLabelName = subLabelName;
	}
	public int getSubCount() {
		return SubCount;
	}
	public void setSubCount(int subCount) {
		SubCount = subCount;
	}
}
